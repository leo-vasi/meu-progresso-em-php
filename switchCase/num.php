<?php

$numero = 3;

switch ($numero) {
    case 1:
        echo "Um";
        break;
    case 2:
        echo "Dois";
        break;
    case 3:
        echo "Três";
        break;
    case 4:
        echo "Quatro";
        break;
    case 5:
        echo "Cinco";
        break;
    default:
        echo "Número fora do intervalo!";
}


?>